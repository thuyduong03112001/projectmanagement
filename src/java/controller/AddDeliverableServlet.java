    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
//        processRequest(request, response);
        int project = Integer.parseInt(request.getParameter("projectAdd"));
        String deliverable = request.getParameter("deliverableAdd");
        String date = request.getParameter("dateAdd");
        int status = Integer.parseInt(request.getParameter("statusAdd"));
        Deliverable de = new Deliverable();
        de.setProjectId(project);
        de.setDeliverable(deliverable);
        de.setPlannedDate(date);
        de.setStatusId(status);
        DeliverableDAO ddb = new DeliverableDAO();
        ddb.addDeliverable(de);
        response.sendRedirect("deliverablelist");
    }

